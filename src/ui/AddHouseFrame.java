    private void addHouseBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addHouseBtnActionPerformed
        // TODO add your handling code here:

        if(txtHouse.getText()== null || txtHouse.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "Please enter all fields ! ");
        }
        else{
            House house = new House();
            house.setHouseName(txtHouse.getText());
            house.setHouseId(getUniqueId());
            house.setHouseCity((String) cityComboBox.getSelectedItem());
            house.setHouseCommunity((String) commComboBox.getSelectedItem());

            String commName = (String) commComboBox.getSelectedItem();
            Community comm = new Community();

            for(Community co: communityDirectory.getCommunityList()){
                if(co.getCommunityAddress().equals(commName)){
                    comm = co;
                    break;
                }
            }

            house.setHouseCityId(comm.getCityID());
            house.setHouseCommId(comm.getCommunityID());

            houseDirectory.addNewHouse(house);

            JOptionPane.showMessageDialog(this, "House has been added");

        }
    }//GEN-LAST:event_addHouseBtnActionPerformed

