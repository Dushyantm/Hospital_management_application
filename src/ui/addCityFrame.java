    private void addCityBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addCityBtnActionPerformed
        // TODO add your handling code here:

        if(txtCity.getText()== null || txtCity.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "Please enter all fields ! ");
        }
        else{

            City c = new City();
            c.setCityName(txtCity.getText());
            c.setCityId(getUniqueId());
            cityList.add(c);
            JOptionPane.showMessageDialog(this, "City has been added");
        }
    }//GEN-LAST:event_addCityBtnActionPerformed

